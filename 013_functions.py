# Произвольное число параметров


def min(*a):
    m = a[0]
    for x in a:
        if m > x:
            m = x
    return m

# Значение параметров по умолчанию


def my_range(start, stop, step=1):
    res = []
    if step > 0:
        x = start
        while x < stop:
            res += [x]
            x += step
    elif step < 0:
        x = start
        while x > stop:
            res += [x]
            x += step
    return res


print(my_range(2, 5))
print(my_range(2, 15, 3))
print(my_range(15, 2, -3))

# Вызывать функции можно также с аргументами не по порядку, но для этого нужно их явно описать
my_range(stop=20, step=2, start=16)


